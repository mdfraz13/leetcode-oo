package leetcode.oo.tree;

/**
 * Tree node.
 */
public interface TreeNode {

    /**
     * Node value.
     *
     * @return Value
     */
    int value();

    /**
     * Left node.
     *
     * @return Left
     */
    TreeNode left();

    /**
     * Right node.
     *
     * @return Right
     */
    TreeNode right();

}
